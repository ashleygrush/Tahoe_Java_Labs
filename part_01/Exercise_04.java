package part_01;

 /**
 * Part 1 Exercise 4:
 *      Write the necessary code to print the following table to the console:
 *
 *      a   a*2 a*4
 *      1   2   4
 *      ....
 *      4   8   16
 *
 */


 public class Exercise_04 {
     public static void main(String[] args) {

         int a = 1;
         int b = 2;
         int c = 3;
         int d = 4;

         System.out.println(a + " " + a*2 + " "  + a*4);
         System.out.println(b + " " + b*2 + " "  + b*4);
         System.out.println(c + " " + c*2 + " "  + c*4);
         System.out.println(d + " " + d*2 + " "  + d*4);
     }
}


